"""Keyword-based complaint categorization with short labels."""

from __future__ import annotations

# (short_label, icon, keywords)
CATEGORIES: list[tuple[str, str, list[str]]] = [
    ("Не работает", "⚡", [
        "не работа", "неработ", "не включ", "не заряж", "перестал",
        "не функцион", "вышел из строя", "не горит", "не свет",
        "не реагир", "не запуск",
    ]),
    ("Брак", "🔧", [
        "брак", "бракован", "дефект", "сломал", "сломан",
        "неисправ", "поломк", "треснул", "лопнул",
    ]),
    ("Качество", "📉", [
        "качеств", "плохое", "низкое", "ужасн", "отврат", "некачествен",
        "разочаров", "хлипк", "тонк", "непрочн", "ненадёжн", "ненадежн",
    ]),
    ("Некомплект", "📦", [
        "комплект", "не хватает", "нет в комплект", "недостаёт", "недостает",
        "отсутств", "не полн", "неполн", "нет в набор",
    ]),
    ("Несоответствие", "🔄", [
        "не соответств", "отличается", "другой цвет", "другой размер",
        "не тот", "не то", "не такой", "на фото", "ожидал", "описани",
    ]),
    ("Повреждение", "💥", [
        "поврежд", "помят", "разбит", "мятый", "грязный", "вскрыт", "порван",
    ]),
    ("Доставка", "🚚", [
        "доставк", "не приш", "потерял", "задерж", "опозда",
    ]),
    ("Размер", "📏", [
        "размер", "велик", "мал", "не подош", "не подход",
        "маленьк", "тесн", "широк", "узк", "большой",
    ]),
]


def categorize_complaint(text: str | None) -> str:
    """Return short label like '⚡ Не работает' or '❓ Без описания'."""
    if not text:
        return "❓ Без описания"

    text_lower = text.lower()

    for label, icon, keywords in CATEGORIES:
        for kw in keywords:
            if kw in text_lower:
                return f"{icon} {label}"

    return "💬 Другое"
