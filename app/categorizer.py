"""Simple keyword-based complaint categorization."""

from __future__ import annotations

# Category -> keywords (lowercase)
CATEGORIES: dict[str, list[str]] = {
    "Брак/Дефект": [
        "брак", "бракован", "дефект", "сломал", "сломан", "не работает",
        "неисправ", "поломк", "не включ", "не заряж", "перестал",
        "не функцион", "вышел из строя", "неработ",
    ],
    "Качество": [
        "качеств", "плохое", "низкое", "ужасн", "отврат", "некачествен",
        "разочаров", "хлипк", "тонк", "непрочн", "ненадёжн", "ненадежн",
    ],
    "Несоответствие": [
        "не соответств", "отличается", "другой цвет", "другой размер",
        "не тот", "не то", "не такой", "на фото", "ожидал", "описани",
    ],
    "Доставка/Упаковка": [
        "доставк", "поврежд", "упаковк", "помят", "разбит", "мятый",
        "грязный", "вскрыт", "порван",
    ],
    "Комплектация": [
        "комплект", "не хватает", "нет в комплект", "недостаёт", "недостает",
        "отсутств", "без ", "не полн", "неполн",
    ],
    "Размер": [
        "размер", "велик", "мал", "не подош", "не подход", "большой",
        "маленьк", "тесн", "широк", "узк",
    ],
}


def categorize_complaint(text: str | None) -> str:
    """Categorize complaint text by keywords. Returns category name or 'Другое'."""
    if not text:
        return "Другое"

    text_lower = text.lower()

    for category, keywords in CATEGORIES.items():
        for kw in keywords:
            if kw in text_lower:
                return category

    return "Другое"
